/* ----------------------------------------------------------------
 * 辅助：RGB565 → XRGB8888 颜色格式转换
 *
 * GEC6818 帧缓冲为 32-bit XRGB8888（高字节忽略，R/G/B 各 8-bit）。
 * 上层 API 统一使用 RGB565（紧凑格式，省内存），在写入帧缓冲前转换。
 * 扩展方法：5→8bit 用 (v<<3)|(v>>2)，6→8bit 用 (v<<2)|(v>>4)，
 * 使低位填充高位，保证白色(0xFFFF) → 0x00FFFFFF，黑色(0) → 0。
 * ---------------------------------------------------------------- */
static inline unsigned int rgb565_to_xrgb(unsigned short c)
{
    unsigned int r = (c >> 11) & 0x1F;
    unsigned int g = (c >>  5) & 0x3F;
    unsigned int b =  c        & 0x1F;
    r = (r << 3) | (r >> 2);   /* 5-bit → 8-bit */
    g = (g << 2) | (g >> 4);   /* 6-bit → 8-bit */
    b = (b << 3) | (b >> 2);   /* 5-bit → 8-bit */
    return (r << 16) | (g << 8) | b;
}

/* ----------------------------------------------------------------
 * 辅助：直接写单个像素（仅在 lcd.c 内部使用）
 * ---------------------------------------------------------------- */
static inline void set_pixel(int x, int y, unsigned short color)
{
    if (x >= 0 && x < g_lcd_width && y >= 0 && y < g_lcd_height)
        s_lcd.pixels[y * g_lcd_width + x] = rgb565_to_xrgb(color);
}

/* ================================================================
 * 初始化 / 清理
 * ================================================================ */

int lcd_open(void)
{
    /* 打开 Linux 帧缓冲设备 */
    s_lcd.fd = open(FB_DEV, O_RDWR);
    if (s_lcd.fd < 0) {
        perror("lcd_open: open " FB_DEV);
        return -1;
    }

    /* 从驱动读取实际分辨率，不依赖编译期硬编码 */
    struct fb_var_screeninfo vinfo;
    if (ioctl(s_lcd.fd, FBIOGET_VSCREENINFO, &vinfo) < 0) {
        perror("lcd_open: ioctl FBIOGET_VSCREENINFO");
        close(s_lcd.fd);
        return -1;
    }
    g_lcd_width  = (int)vinfo.xres;
    g_lcd_height = (int)vinfo.yres;

    /* GEC6818 帧缓冲为 32-bit XRGB8888，每像素 4 字节 */
    size_t fb_size = (size_t)(g_lcd_width * g_lcd_height) * sizeof(unsigned int);

    /*
     * 映射显存到用户态：
     * 后续所有绘图操作都直接写这块内存，不再频繁 write()/ioctl()。
     */
    s_lcd.pixels = (unsigned int *)mmap(NULL, fb_size,
                        PROT_READ | PROT_WRITE, MAP_SHARED, s_lcd.fd, 0);
    if (s_lcd.pixels == MAP_FAILED) {
        perror("lcd_open: mmap");
        close(s_lcd.fd);
        return -1;
    }
    return 0;
}

void lcd_close(void)
{
    /* 与 lcd_open 成对释放资源 */
    size_t fb_size = (size_t)(g_lcd_width * g_lcd_height) * sizeof(unsigned int);
    munmap(s_lcd.pixels, fb_size);
    close(s_lcd.fd);
}

/* ================================================================
 * 基础绘图
 * ================================================================ */

void lcd_fill_rect(int x, int y, int w, int h, unsigned short color)
{
    /* 通过 set_pixel 的边界保护，允许输入区域部分越界 */
    for (int row = y; row < y + h; row++)
        for (int col = x; col < x + w; col++)
            set_pixel(col, row, color);
}

void lcd_draw_border(int x, int y, int w, int h, int thickness, unsigned short color)
{
    for (int t = 0; t < thickness; t++) {
        lcd_fill_rect(x,               y + t,           w, 1, color); /* 上 */
        lcd_fill_rect(x,               y + h - 1 - t,   w, 1, color); /* 下 */
        lcd_fill_rect(x + t,           y,               1, h, color); /* 左 */
        lcd_fill_rect(x + w - 1 - t,   y,               1, h, color); /* 右 */
    }
}

void lcd_draw_hline(int x, int y, int len, int thickness, unsigned short color)
{
    lcd_fill_rect(x, y, len, thickness, color);
}

/* ================================================================
 * 单像素 / 图像块绘制
 * ================================================================ */

void lcd_draw_pixel(int x, int y, unsigned short color)
{
    set_pixel(x, y, color);
}

/**
 * lcd_draw_image - 批量写入 RGB565 像素缓冲区到屏幕矩形区域
 *
 * 优化策略：
 *   - 先裁剪再绘制，只处理屏幕可见区域
 *   - 仅在目标区域与屏幕有交集时才写入，边界安全
 *   - 源缓冲区行偏移与目标裁剪独立计算，不依赖额外辅助缓冲
 *   - 由于源是 RGB565、帧缓冲是 XRGB8888，按像素转换写入
 */
void lcd_draw_image(int x, int y, int w, int h, const unsigned short *pixels)
{
    /* 计算实际写入区域（与屏幕矩形取交集，防止越界） */
    int x0 = x < 0 ? 0 : x;
    int y0 = y < 0 ? 0 : y;
    int x1 = (x + w) > g_lcd_width  ? g_lcd_width  : (x + w);
    int y1 = (y + h) > g_lcd_height ? g_lcd_height : (y + h);

    if (x0 >= x1 || y0 >= y1)
        return;

    int copy_w = x1 - x0;

    /* 帧缓冲是 32 位，源图是 16 位，逐像素转换后写入 */
    for (int row = y0; row < y1; row++) {
        const unsigned short *src = pixels + (row - y) * w + (x0 - x);
        unsigned int         *dst = s_lcd.pixels + row * g_lcd_width + x0;
        for (int col = 0; col < copy_w; col++)
            dst[col] = rgb565_to_xrgb(src[col]);
    }
}


void lcd_draw_char(int x, int y, char c, unsigned short fg, unsigned short bg)
{
    /* 小写自动转大写；超出字模范围则渲染空格 */
    char uc  = (c >= 'a' && c <= 'z') ? (char)(c - 32) : c;
    int  idx = (uc >= 0x20 && uc <= 0x5A) ? (uc - 0x20) : 0;

    for (int row = 0; row < 16; row++) {
        unsigned char line = s_font8x16[idx][row];
        for (int col = 0; col < 8; col++) {
            /* bit7 → 最左列，依次向右 */
            unsigned short pixel = (line & (0x80 >> col)) ? fg : bg;
            set_pixel(x + col, y + row, pixel);
        }
    }
}

void lcd_draw_string(int x, int y, const char *str,
                     unsigned short fg, unsigned short bg)
{
    for (int i = 0; str[i] != '\0'; i++)
        lcd_draw_char(x + i * 8, y, str[i], fg, bg);
}
